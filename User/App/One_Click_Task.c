 //参数 joint_idx 范围：1~6 (对应 q1~q6)
float get_joint_position(int joint_idx, float current_t, float T_TOTAL, int NUM_SEGMENTS ,const float* t_knots, const float* q_coeffs ,float scale) {
	//current_t从零开始
    int j_idx = joint_idx - 1;
    if (j_idx < 0) j_idx = 0;
    if (j_idx > 5) j_idx = 5;
    
    if (current_t <= 0.0f) current_t = 0.0f;
    if (current_t >= T_TOTAL) current_t = T_TOTAL - 0.0001f;
    
    int seg = 0;
    for(int i = 0; i < NUM_SEGMENTS; i++) {
        if (current_t >= t_knots[i] && current_t < t_knots[i+1]) {
            seg = i;
            break;
        }
    }
    
    float dt = ( current_t - t_knots[seg] ) * scale;
    int coeff_offset = (j_idx * NUM_SEGMENTS + seg) * 4;
		
    float a = ((float*)q_coeffs)[coeff_offset + 0];
    float b = ((float*)q_coeffs)[coeff_offset + 1];
    float c = ((float*)q_coeffs)[coeff_offset + 2];
    float d = ((float*)q_coeffs)[coeff_offset + 3];
    
    return ((a * dt + b) * dt + c) * dt + d;
}
 
